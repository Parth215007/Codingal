import math
print(f"For the input of 23.56 math.ceil returns: {math.ceil(23.56)}")

print(f"For the input of 23.56 math.floor returns: {math.floor(23.56)}")

x = 7
y = -1
print(f"The value of x after copying the sign of y is: {math.copysign(x,y)}")

print(f"Absolute value of -7 and 1 is: {math.fabs(-7)} and {math.fabs(1)}")

print(f"The H.C.F of 65 and 85 is: {math.gcd(65,85)}")